/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode(int x) : val(x), left(NULL), right(NULL) {}
 * };
 */
class Solution {
public:
    bool isUnivalTree(TreeNode* root) {
        if(!root) return true;
        int value = root->val;
        
        return isSame(root, value);
    }

    bool isSame(TreeNode* root, int val)
    {
        if(!root) return true;
        if(val != root->val) return false;
        else {return isSame(root->left, val) && isSame(root->right, val);}
    }
};

